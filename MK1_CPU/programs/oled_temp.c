unsigned char glyph_buf[30];

void send_glyph(unsigned char idx) {
    // Read 5 font bytes and send via I2C
    unsigned char base;
    unsigned char i;
    base = idx * 5 + 135;
    i2c_start();
    i2c_send_byte(0x78);
    i2c_send_byte(0x40);
    for (i = 0; i < 5; i++) {
        i2c_send_byte(peek3(base + i));
    }
    i2c_send_byte(0x00);
    i2c_stop();
}

void main() {
    unsigned char temp;
    unsigned char tens;
    unsigned char ones;

    i2c_init();
    i2c_bus_reset();
    i2c_stream(0);
    i2c_stream(21);

    i2c_stream(126);
    temp = i2c_stream_result();

    tens = 0;
    ones = temp;
    while (ones >= 10) {
        ones = ones - 10;
        tens = tens + 1;
    }

    // Set position: page 3, col 40
    i2c_start();
    i2c_send_byte(0x78);
    i2c_send_byte(0x00);
    i2c_send_byte(0xB3);
    i2c_send_byte(0x08);
    i2c_send_byte(0x12);
    i2c_stop();

    if (tens > 0) {
        send_glyph(tens);
    } else {
        send_glyph(10);
    }
    send_glyph(ones);
    send_glyph(11);
    send_glyph(12);

    out(temp);
    halt();
}
