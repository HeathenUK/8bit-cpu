void send_glyph(unsigned char idx) {
    unsigned char base;
    unsigned char i;
    base = idx * 5 + 143;
    i = 0;
    while (i < 5) {
        poke3(peek3(base + i), 208 + 3 + i);
        i = i + 1;
    }
    i2c_stream(208);
}

// Factor display logic out of main so Phase 7 / overlay can place it.
void show_temp(unsigned char temp) {
    unsigned char tens;
    unsigned char ones;
    tens = 0;
    ones = temp;
    while (ones >= 10) { ones = ones - 10; tens = tens + 1; }
    i2c_stream(135);   // position
    if (tens > 0) { send_glyph(tens); }
    else          { send_glyph(10); }
    send_glyph(ones);
    send_glyph(11);
    send_glyph(12);
}

void main() {
    unsigned char t;
    i2c_init();
    i2c_bus_reset();
    i2c_stream(0);      // OLED init
    i2c_stream(21);     // clear
    i2c_stream(126);    // temp read
    t = i2c_stream_result();
    show_temp(t);
    out(t);
    halt();
}
